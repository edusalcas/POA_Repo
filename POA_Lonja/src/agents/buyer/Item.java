package agents.buyer;

public class Item {

	private String name;
	private float cantidad;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public float getCantidad() {
		return cantidad;
	}
	public void setCantidad(float cantidad) {
		this.cantidad = cantidad;
	}
	
	
}
